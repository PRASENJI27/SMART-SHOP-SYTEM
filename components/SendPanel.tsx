
import React, { useState } from 'react';
import { ShoppingItem } from '../types';
import { jsPDF } from 'https://esm.sh/jspdf';

interface SendPanelProps {
  isDark: boolean;
  items: ShoppingItem[];
  targetNumber: string;
  setTargetNumber: (num: string) => void;
}

const SendPanel: React.FC<SendPanelProps> = ({ isDark, items, targetNumber, setTargetNumber }) => {
  const [isCopied, setIsCopied] = useState(false);
  const [isGeneratingPdf, setIsGeneratingPdf] = useState(false);

  const formatListText = () => {
    if (items.length === 0) return "My shopping list is currently empty.";
    
    let text = "ðŸ›’ *My SmartShop List:*\n\n";
    const grouped = items.reduce((acc, item) => {
      if (!acc[item.category]) acc[item.category] = [];
      acc[item.category].push(item);
      return acc;
    }, {} as Record<string, ShoppingItem[]>);

    Object.entries(grouped).forEach(([cat, catItems]) => {
      text += `*${cat.toUpperCase()}*\n`;
      catItems.forEach(item => {
        text += `${item.completed ? 'âœ…' : 'â¬œ'} ${item.name} (${item.quantity})\n`;
      });
      text += '\n';
    });

    return text;
  };

  const handleSendWhatsApp = () => {
    const text = encodeURIComponent(formatListText());
    window.open(`https://wa.me/${targetNumber}?text=${text}`, '_blank');
  };

  const handleSendSMS = () => {
    const text = encodeURIComponent(formatListText());
    window.open(`sms:${targetNumber}?body=${text}`, '_self');
  };

  const generatePDF = async () => {
    if (items.length === 0) return;
    setIsGeneratingPdf(true);
    
    try {
      const doc = new jsPDF();
      const timestamp = new Date().toLocaleString();
      
      // Header
      doc.setFontSize(22);
      doc.setTextColor(79, 70, 229); // Indigo 600
      doc.text("SmartShop List", 20, 20);
      
      doc.setFontSize(10);
      doc.setTextColor(100, 116, 139); // Slate 500
      doc.text(`Generated on: ${timestamp}`, 20, 28);
      
      let y = 40;
      const grouped = items.reduce((acc, item) => {
        if (!acc[item.category]) acc[item.category] = [];
        acc[item.category].push(item);
        return acc;
      }, {} as Record<string, ShoppingItem[]>);

      Object.entries(grouped).forEach(([category, catItems]) => {
        if (y > 270) { doc.addPage(); y = 20; }
        
        doc.setFontSize(12);
        doc.setTextColor(30, 41, 59); // Slate 800
        doc.setFont("helvetica", "bold");
        doc.text(category.toUpperCase(), 20, y);
        y += 8;
        
        doc.setFont("helvetica", "normal");
        doc.setFontSize(10);
        catItems.forEach(item => {
          if (y > 280) { doc.addPage(); y = 20; }
          const status = item.completed ? "[X]" : "[ ]";
          doc.text(`${status} ${item.name} (${item.quantity})`, 25, y);
          y += 6;
        });
        y += 6;
      });

      doc.save(`SmartShop_List_${new Date().getTime()}.pdf`);
    } catch (error) {
      console.error("PDF Error:", error);
    } finally {
      setIsGeneratingPdf(false);
    }
  };

  const handleCopy = () => {
    navigator.clipboard.writeText(formatListText());
    setIsCopied(true);
    setTimeout(() => setIsCopied(false), 2000);
  };

  return (
    <div className={`p-6 border-t transition-colors duration-300 flex flex-col gap-6 ${
      isDark ? 'bg-slate-950 border-slate-800' : 'bg-slate-50 border-slate-100'
    }`}>
      
      {/* Target Number Input Area */}
      <div className="flex flex-col sm:flex-row items-center gap-4">
        <div className="flex-1 w-full">
          <label className={`text-xs font-bold uppercase tracking-wider mb-2 block ${isDark ? 'text-slate-500' : 'text-slate-400'}`}>
            Share to Phone Number
          </label>
          <div className="relative">
            <span className="absolute left-4 top-1/2 -translate-y-1/2 text-slate-500 font-bold">+</span>
            <input
              type="tel"
              value={targetNumber}
              onChange={(e) => setTargetNumber(e.target.value)}
              placeholder="e.g. 897164272"
              className={`w-full pl-8 pr-4 py-3 rounded-xl border transition-all outline-none focus:ring-2 focus:ring-indigo-500 ${
                isDark 
                  ? 'bg-slate-900 border-slate-800 text-white placeholder-slate-700' 
                  : 'bg-white border-slate-200 text-slate-800 placeholder-slate-300'
              }`}
            />
          </div>
        </div>
        <button
          onClick={generatePDF}
          disabled={items.length === 0 || isGeneratingPdf}
          className={`w-full sm:w-auto mt-6 px-6 py-3 rounded-xl border font-semibold transition-all flex items-center justify-center gap-2 ${
            isDark 
              ? 'border-indigo-900 bg-indigo-900/20 text-indigo-400 hover:bg-indigo-900/40' 
              : 'border-indigo-100 bg-indigo-50 text-indigo-600 hover:bg-indigo-100'
          } disabled:opacity-30`}
        >
          {isGeneratingPdf ? (
            <div className="w-5 h-5 border-2 border-indigo-400 border-t-transparent rounded-full animate-spin"></div>
          ) : (
            <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M7 21h10a2 2 0 002-2V9.414a1 1 0 00-.293-.707l-5.414-5.414A1 1 0 0012.586 3H7a2 2 0 00-2 2v14a2 2 0 002 2z" />
            </svg>
          )}
          PDF
        </button>
      </div>

      <div className="grid grid-cols-1 sm:grid-cols-3 gap-3">
        <button
          onClick={handleCopy}
          className={`px-5 py-3 rounded-xl border font-bold transition-all flex items-center justify-center gap-2 ${
            isDark 
              ? 'border-slate-800 bg-slate-900 text-slate-400 hover:text-white hover:bg-slate-800' 
              : 'border-slate-200 bg-white text-slate-600 hover:bg-slate-50'
          }`}
        >
          <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 5H6a2 2 0 00-2 2v12a2 2 0 002 2h10a2 2 0 002-2v-1M8 5a2 2 0 002 2h2a2 2 0 002-2M8 5a2 2 0 012-2h2a2 2 0 012 2m0 0h2a2 2 0 012 2v3m2 4H10m0 0l3-3m-3 3l3 3" />
          </svg>
          {isCopied ? 'Copied' : 'Copy Text'}
        </button>

        <button
          onClick={handleSendSMS}
          disabled={items.length === 0}
          className="px-5 py-3 rounded-xl bg-blue-600 text-white font-bold hover:bg-blue-700 active:scale-95 transition-all shadow-lg shadow-blue-900/20 disabled:opacity-30 flex items-center justify-center gap-2"
        >
          <svg xmlns="http://www.w3.org/2000/svg" className="h-5 w-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M8 10h.01M12 10h.01M16 10h.01M9 16H5a2 2 0 01-2-2V6a2 2 0 012-2h14a2 2 0 012 2v8a2 2 0 01-2 2h-5l-5 5v-5z" />
          </svg>
          Message
        </button>

        <button
          onClick={handleSendWhatsApp}
          disabled={items.length === 0}
          className="px-5 py-3 rounded-xl bg-green-600 text-white font-bold hover:bg-green-700 active:scale-95 transition-all shadow-lg shadow-green-900/20 disabled:opacity-30 flex items-center justify-center gap-2"
        >
          <svg viewBox="0 0 24 24" className="h-5 w-5 fill-current">
            <path d="M17.472 14.382c-.297-.149-1.758-.867-2.03-.967-.273-.099-.471-.148-.67.15-.197.297-.767.966-.94 1.164-.173.199-.347.223-.644.075-.297-.15-1.255-.463-2.39-1.475-.883-.788-1.48-1.761-1.653-2.059-.173-.297-.018-.458.13-.606.134-.133.298-.347.446-.52.149-.174.198-.298.298-.497.099-.198.05-.371-.025-.52-.075-.149-.669-1.612-.916-2.207-.242-.579-.487-.5-.669-.51-.173-.008-.371-.01-.57-.01-.198 0-.52.074-.792.372-.272.297-1.04 1.016-1.04 2.479 0 1.462 1.065 2.875 1.213 3.074.149.198 2.096 3.2 5.077 4.487.709.306 1.262.489 1.694.625.712.227 1.36.195 1.871.118.571-.085 1.758-.719 2.006-1.413.248-.694.248-1.289.173-1.413-.074-.124-.272-.198-.57-.347m-5.421 7.403h-.004a9.87 9.87 0 01-5.031-1.378l-.361-.214-3.741.982.998-3.648-.235-.374a9.86 9.86 0 01-1.51-5.26c.001-5.45 4.436-9.884 9.888-9.884 2.64 0 5.122 1.03 6.988 2.898a9.825 9.825 0 012.893 6.994c-.003 5.45-4.437 9.884-9.885 9.884m8.413-18.297A11.815 11.815 0 0012.05 0C5.495 0 .16 5.335.157 11.892c0 2.096.547 4.142 1.588 5.945L.057 24l6.305-1.654a11.882 11.882 0 005.683 1.448h.005c6.554 0 11.89-5.335 11.893-11.893a11.821 11.821 0 00-3.48-8.413Z"/>
          </svg>
          WhatsApp
        </button>
      </div>
    </div>
  );
};

export default SendPanel;
